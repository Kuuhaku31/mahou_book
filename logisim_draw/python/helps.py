# 打印帮助信息
def 打印帮助信息():
    help_text = """
格式: python main.py -m <模式> -t <目标circ文件地址> -p <待加载图像地址> -l <目标标签名称>

### 参数

| 参数        | 功能                      |
| ----------- | ------------------------- |
| -m          | 指定模式                  |
| -t          | 指定目标文件地址          |
| -s          | 指定源文件地址            |
| -l          | 指定目标 circuit 标签名称 |
| -h --help   | 打印帮助信息              |
| -rm_current | 是否删除原先的像素信息    |

### 模式

| 模式 | 功能                                             | -t                 | -s                 | -l               | -rm_current | -h --help    |
| ---- | ------------------------------------------------ | ------------------ | ------------------ | ---------------- | ----------- | ------------ |
| del  | 删除指定目标 circuit 标签下的所有像素            | circ 文件地址      | 无效参数           | circuit 标签名称 | 无效        | 打印帮助信息 |
| add  | 将图片添加到目标 circ 文件                       | circ 文件地址      | 待加载图像地址     | circuit 标签名称 | 有效        | 打印帮助信息 |
| conv | 将图片转换为像素信息                             | 像素库的文件夹路径 | 待加载图像地址     | circuit 标签名称 | 无效        | 打印帮助信息 |
| load | 从像素库的 html 文件加载像素信息到目标 circ 文件 | circ 文件地址      | 像素库的文件夹路径 | circuit 标签名称 | 有效        | 打印帮助信息 |

"""

    print(help_text)


# 每个模式下的启动失败信息
def 打印模式启动失败信息(模式):
    if 模式 == "del":
        print("删除模式启动失败: 请提供 -t 和 -l 参数")
    elif 模式 == "add":
        print("添加模式启动失败: 请提供 -t、-l 和 -s 参数")
    elif 模式 == "conv":
        print("转换模式启动失败: 请提供 -t、-s 和 -l 参数")
    elif 模式 == "load":
        print("加载模式启动失败: 请提供 -t、-s 和 -l 参数")
    elif 模式 == "":
        print("未指定模式，请使用 -m 参数指定")
    else:
        print(f"未知模式: {模式}")

    打印帮助信息()
